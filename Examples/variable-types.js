// Variable is a symbolic name for a value.
// Variable are declared with var keyword:
var = x;                        // Declare a variable named X.

// Values can be assigned to variables with an = sign
x = 0;                          // Now the variable X has the value 0
x                               // => 0: A variable evaluates to its value.

// Javascript support several types of values
x = 1;                          // Numbers
x = 0.01;                       // Just one Number type of integers and reals.
x = "Hello World";              // Strings of text in quotation marks.
x = 'JavaScript';               // Single quote mark also delimit strings.
x = true;                       // Boolean value.
x = false;                      // The othe boolean value.
x = null;                       // Null is a special value that means "no value".
x = undefined;                  // Undefined is like null.